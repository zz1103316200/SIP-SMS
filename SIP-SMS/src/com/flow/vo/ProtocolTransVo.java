package com.flow.vo;

public class ProtocolTransVo {
	String protocolTransName;
	String inputProtocol;
	String outputProtocol;
	String protocolTransInfo;
	String protocolTransStatus;
	String protocolTransDesc;
	public String getProtocolTransName() {
		return protocolTransName;
	}
	public void setProtocolTransName(String protocolTransName) {
		this.protocolTransName = protocolTransName;
	}
	public String getInputProtocol() {
		return inputProtocol;
	}
	public void setInputProtocol(String inputProtocol) {
		this.inputProtocol = inputProtocol;
	}
	public String getOutputProtocol() {
		return outputProtocol;
	}
	public void setOutputProtocol(String outputProtocol) {
		this.outputProtocol = outputProtocol;
	}
	public String getProtocolTransInfo() {
		return protocolTransInfo;
	}
	public void setProtocolTransInfo(String protocolTransInfo) {
		this.protocolTransInfo = protocolTransInfo;
	}
	public String getProtocolTransStatus() {
		return protocolTransStatus;
	}
	public void setProtocolTransStatus(String protocolTransStatus) {
		this.protocolTransStatus = protocolTransStatus;
	}
	public String getProtocolTransDesc() {
		return protocolTransDesc;
	}
	public void setProtocolTransDesc(String protocolTransDesc) {
		this.protocolTransDesc = protocolTransDesc;
	}
}
