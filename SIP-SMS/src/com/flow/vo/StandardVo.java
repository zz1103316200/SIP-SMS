package com.flow.vo;

public class StandardVo {
	String dataType;
	String dataTypeContent;
	String dataTypeDescription;
	String dataTypeStatus;
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public String getDataTypeContent() {
		return dataTypeContent;
	}
	public void setDataTypeContent(String dataTypeContent) {
		this.dataTypeContent = dataTypeContent;
	}
	public String getDataTypeDescription() {
		return dataTypeDescription;
	}
	public void setDataTypeDescription(String dataTypeDescription) {
		this.dataTypeDescription = dataTypeDescription;
	}
	public String getDataTypeStatus() {
		return dataTypeStatus;
	}
	public void setDataTypeStatus(String dataTypeStatus) {
		this.dataTypeStatus = dataTypeStatus;
	}
	
}
